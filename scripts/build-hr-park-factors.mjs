import { mkdir, readFile, writeFile } from 'node:fs/promises';
import { existsSync } from 'node:fs';
import path from 'node:path';
import {
  normalizeParkFactorVenue,
  parseNpbGameDetail,
} from '../shared/hrParkFactor.js';

const ROOT = path.resolve(import.meta.dirname, '..');
const CACHE_DIR = path.join(ROOT, '.cache', 'hr-park-factors');
const OUTPUT = path.join(ROOT, 'src', 'data', 'hrParkFactors.generated.js');
const YEARS = (process.env.YEARS ?? '2023,2024,2025').split(',').map(Number);
const MONTHS = ['03', '04', '05', '06', '07', '08', '09', '10'];
const CONCURRENCY = 10;
const REGRESSION_GAMES = 60;

async function fetchText(url, cachePath) {
  if (existsSync(cachePath)) return readFile(cachePath, 'utf8');
  const response = await fetch(url, {
    headers: { 'User-Agent': 'npbinfo-park-factor-builder/1.0' },
  });
  if (!response.ok) throw new Error(`${response.status} ${url}`);
  const text = await response.text();
  await mkdir(path.dirname(cachePath), { recursive: true });
  await writeFile(cachePath, text);
  return text;
}

async function schedulePaths(year) {
  const paths = new Set();
  for (const month of MONTHS) {
    const url = `https://npb.jp/games/${year}/schedule_${month}_detail.html`;
    const html = await fetchText(url, path.join(CACHE_DIR, `${year}-${month}.html`));
    for (const match of html.matchAll(/href="(\/scores\/\d{4}\/\d{4}\/[^/]+\/)"/g)) {
      paths.add(match[1]);
    }
  }
  return [...paths].sort();
}

async function mapLimit(items, limit, mapper) {
  const results = new Array(items.length);
  let cursor = 0;
  async function worker() {
    while (cursor < items.length) {
      const index = cursor;
      cursor += 1;
      results[index] = await mapper(items[index], index);
    }
  }
  await Promise.all(Array.from({ length: limit }, worker));
  return results;
}

function buildFactors(games) {
  const venues = {};
  const road = {};

  for (const game of games) {
    const totalHr = game.homeHr + game.awayHr;
    const venue = venues[game.venue] ??= { games: 0, homeRuns: 0, hosts: {} };
    venue.games += 1;
    venue.homeRuns += totalHr;
    venue.hosts[game.homeTeam] = (venue.hosts[game.homeTeam] ?? 0) + 1;

    const teamRoad = road[game.awayTeam] ??= { games: 0, homeRuns: 0 };
    teamRoad.games += 1;
    teamRoad.homeRuns += totalHr;
  }

  const observed = Object.fromEntries(Object.entries(venues).map(([venue, data]) => {
    const roadRate = Object.entries(data.hosts).reduce((sum, [team, count]) => {
      const context = road[team];
      return sum + count * (context?.games ? context.homeRuns / context.games : 0);
    }, 0) / data.games;
    const parkRate = data.homeRuns / data.games;
    const rawFactor = roadRate > 0 ? parkRate / roadRate : 1;
    const weight = data.games / (data.games + REGRESSION_GAMES);
    return [venue, {
      games: data.games,
      homeRuns: data.homeRuns,
      rawFactor,
      regressedFactor: 1 + (rawFactor - 1) * weight,
    }];
  }));

  const totalGames = Object.values(observed).reduce((sum, value) => sum + value.games, 0);
  const mean = Object.values(observed)
    .reduce((sum, value) => sum + value.regressedFactor * value.games, 0) / totalGames;

  return Object.fromEntries(Object.entries(observed)
    .sort(([, a], [, b]) => b.games - a.games)
    .map(([venue, value]) => [normalizeParkFactorVenue(venue), {
      factor: Math.round((value.regressedFactor / mean) * 1000) / 1000,
      games: value.games,
      homeRuns: value.homeRuns,
      observed: Math.round(value.rawFactor * 1000) / 1000,
    }]));
}

await mkdir(CACHE_DIR, { recursive: true });
const allPaths = (await Promise.all(YEARS.map(schedulePaths))).flat();
console.log(`detail pages: ${allPaths.length}`);

const games = (await mapLimit(allPaths, CONCURRENCY, async (gamePath, index) => {
  const cacheName = gamePath.replace(/^\/|\/$/g, '').replaceAll('/', '-') + '.html';
  const html = await fetchText(`https://npb.jp${gamePath}`, path.join(CACHE_DIR, cacheName));
  if ((index + 1) % 100 === 0) console.log(`fetched: ${index + 1}/${allPaths.length}`);
  return parseNpbGameDetail(html, gamePath);
})).filter(Boolean);

const factors = buildFactors(games);
const generatedAt = new Date().toISOString();
const output = `// Generated by scripts/build-hr-park-factors.mjs\n`
  + `export const HR_PARK_FACTOR_META = ${JSON.stringify({
    years: YEARS,
    generatedAt,
    regressionGames: REGRESSION_GAMES,
    games: games.length,
    method: 'three-year home/road HR per game, regressed toward 1.000',
  }, null, 2)};\n\n`
  + `export const HR_PARK_FACTORS = ${JSON.stringify(factors, null, 2)};\n`;
await writeFile(OUTPUT, output);
console.log(`eligible games: ${games.length}`);
console.log(`venues: ${Object.keys(factors).length}`);
console.log(`wrote: ${OUTPUT}`);
